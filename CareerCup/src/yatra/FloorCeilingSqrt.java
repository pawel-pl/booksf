package yatra;

public class FloorCeilingSqrt {

	/**
	 * @param args
	 */
	public static void main(String[] args) {
		
		double n = 10.1;
		double n2 = -10.1;
		System.out.println(Math.floor(n));
		System.out.println(Math.ceil(n));
		
		System.out.println(Math.floor(n2));
		System.out.println(Math.ceil(n2));

	}

}
